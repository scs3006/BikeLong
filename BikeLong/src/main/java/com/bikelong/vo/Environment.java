package com.bikelong.vo;


public class Environment {

	private int environmentNo;
	private int rentalshopNo;
	private float temperature;
	private float humidity;
	private float dust;
	private int time;
	private String date;
	private String rentalshopName;
	
	public int getEnvironmentNo() {
		return environmentNo;
	}
	public void setEnvironmentNo(int environmentNo) {
		this.environmentNo = environmentNo;
	}
	public int getRentalshopNo() {
		return rentalshopNo;
	}
	public void setRentalshopNo(int rentalshopNo) {
		this.rentalshopNo = rentalshopNo;
	}
	public float getTemperature() {
		return temperature;
	}
	public void setTemperature(float temperature) {
		this.temperature = temperature;
	}
	public float getHumidity() {
		return humidity;
	}
	public void setHumidity(float humidity) {
		this.humidity = humidity;
	}
	public float getDust() {
		return dust;
	}
	public void setDust(float dust) {
		this.dust = dust;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getRetalshopName() {
		return rentalshopName;
	}
	public void setRetalshopName(String rentalshopName) {
		this.rentalshopName = rentalshopName;
	}
	
	
}
